def is_filial(team_name: str) -> bool:
    if not team_name:
        return False
    name = team_name.strip()
    if name in FILIALES_EXACT:
        return True
    return name.endswith(" B") or name.endswith(" II")


def canonical(team_name: str) -> str:
    """Devuelve el nombre canonico para enlazar fuentes diferentes."""
    if not team_name:
        return ""
    name = team_name.strip()
    return TEAM_ALIASES.get(name, name)

